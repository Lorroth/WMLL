
@SuppressWarnings("serial")
public class WMLLException extends Exception {

	public WMLLException() {
	}

	public WMLLException(String message) {
		super(message);
	}

	public WMLLException(Throwable cause) {
		super(cause);
	}

	public WMLLException(String message, Throwable cause) {
		super(message, cause);
	}

}
